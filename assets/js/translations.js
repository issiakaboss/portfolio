// Fonction globale pour changer la langue (appelée par tes boutons)
function changeLanguage(lang) {
    localStorage.setItem('preferredLang', lang);
    applyTranslations(lang);
}

// Fonction qui parcourt le DOM pour traduire
function applyTranslations(lang) {
    const elements = document.querySelectorAll('[data-key]');
    elements.forEach(el => {
        const key = el.getAttribute('data-key');
        if (translations[lang][key]) {
            // Gestion spéciale pour les placeholders (champs de contact)
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });
  document.documentElement.lang = lang;
  // Dans ta fonction applyTranslations
const typedEl = document.querySelector('.typed');
if (typedEl) {
    typedEl.setAttribute('data-typed-items', translations[lang]['hero_typed']);
}
}

// Au chargement de la page : on applique la langue sauvegardée ou FR par défaut
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLang') || 'fr';
    applyTranslations(savedLang);
});